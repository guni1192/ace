pub fn print_help() {
    println!("Usage ArchLinux Container Engine(ACE)");
    println!("  -h \t --help \t print help message");
    println!("  -n \t --name \t container name");
    println!("     \t --exec \t container run command (default /bin/bash)");
    println!("     \t --init \t bootstrap container os");
    println!("     \t --del  \t delete container");
}
