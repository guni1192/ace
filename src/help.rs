pub fn print_help() {
    println!("Usage ArchLinux Container Engine(ACE)");
    println!("  --h --help \t print help message");
    println!("      --name \t container name");
    println!("      --exec \t container run command (default /bin/bash)");
    println!("      --init \t bootstrap container os");
    println!("      --del  \t delete container");
}
